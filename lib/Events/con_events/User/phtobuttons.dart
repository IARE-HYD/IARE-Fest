              children: <Widget>[
                new Image.network(
                  'https://cdn.mos.cms.futurecdn.net/gvQ9NhQP8wbbM32jXy4V3j.jpg',
                  fit: BoxFit.cover,
                  height: 250.0,
                ),
                twoButtonsSection,
              ],
            ))); //Widget with "Material design"
